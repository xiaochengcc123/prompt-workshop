import type { Duty, Job } from "@/lib/jobs";

export function buildPlan(job: Job, duty: Duty, prompt: string) {
  return `# ${job.name} · ${duty.title} 执行方案

## 背景与目标
当前职责聚焦于：${duty.text}
本方案基于已生成提示词，目标是把职责要求转成一套可落地的执行动作、交付物和推进节奏。

## 执行原则
- 结合${job.name}的业务目标，优先处理影响最大的事项。
- 保持跨部门协作清晰，明确责任人、输入和输出。
- 每一步都要有可交付成果，而不是停留在抽象建议。

## 分阶段方案
### 第一阶段：需求澄清
1. 复核业务背景、时间范围、关键指标和现有资源。
2. 与相关角色确认边界条件，例如审批要求、协作方、数据来源。
3. 输出《需求确认清单》。

### 第二阶段：方案设计
1. 根据提示词拆出 3 到 5 个核心工作模块。
2. 为每个模块定义目标、执行动作、负责人和交付物。
3. 输出《执行方案表》。

### 第三阶段：落地执行
1. 按优先级推进重点任务，先处理高价值环节。
2. 每个模块同步进展、风险和阻塞项。
3. 输出阶段性成果，例如文档、表格、话术、原型或复盘。

### 第四阶段：复盘优化
1. 对照验收指标检查完成度。
2. 记录问题、偏差和改进建议。
3. 输出《复盘与优化建议》。

## 交付物建议
- 需求确认清单
- 执行计划表
- 周进度追踪表
- 风险与应对清单
- 阶段复盘模板

## 风险与应对
- 信息不完整：先补齐关键输入，避免直接执行造成返工。
- 协作角色不清：明确责任人和沟通节点。
- 结果无法衡量：在执行前定义验收指标。

## 参考提示词
${prompt}`;
}
